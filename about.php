    <h1>About Sushu</h1>
    <p>Sushu is een sushi-restaurant gevestigd in Nijmegen, Nederland. Onze missie is om onze klanten de hoogste
        kwaliteit sushi te bieden terwijl we een gastvrije en comfortabele omgeving creëren.</p>

    <p>Ons team van ervaren chefs maakt elke sushirol met de hand en gebruikt alleen de meest verse ingrediënten, zodat
        elke hap een heerlijke en gedenkwaardige ervaring is. Van klassieke favorieten zoals California rolls tot unieke
        creaties zoals onze pittige tonijn- en avocado-rol, er is voor elk wat wils op ons menu.</p>

    <p> Naast onze uitstekende sushi bieden we een verscheidenheid aan andere Japanse gerechten en drankjes, waaronder
        sake en groene thee. Of je nu zin hebt in een snelle lunch of een ontspannen diner, we nodigen je uit om te
        komen en de beste Japanse keuken te ervaren bij Sushu.</p>

    <p> Bedankt voor het overwegen van Sushu voor je volgende eetervaring. We kijken ernaar uit om je van dienst te
        zijn!</p>
